% ssget: MATLAB interface for the SuiteSparse Matrix Collection.
%
%   ssget          - loads a matrix from the SuiteSparse Matrix Collection.
%   ssweb          - opens the URL for a matrix in the SuiteSparse Matrix Collection.
%   ssgrep         - search for matrices in the SuiteSparse Matrix Collection.
%   sskinds        - get 'kind' of each problem in the SuiteSparse Matrix Collection.
%   sssvd          - singular values of a matrix in the SuiteSparse Matrix Collection.
%
% Helper functions:
%
%   ssget_defaults - returns default parameter settings for ssget.
%   ssget_example  - a demo for ssget.
%   ssget_lookup   - gets the group, name, and id of a matrix.
%
% Example:
%   help ssget

